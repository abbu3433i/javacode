public class a_7leftshift {
    public static void main(String[] args) {
    /**********gerneral formula of left shift operator*********/ 
    // 2^n*given integer

        int x = 4;
        System.out.println(x<<0);  // 2
        System.out.println(x<<1);  // 4
        System.out.println(x<<2);  // 8
        System.out.println(x<<10);  // 4096 -> "2^10*4"




    /******right shift*****/
    //  general formula of right shift---> (2^n)/2

        int y = 8>>2;
        // int y = 8>>0;
        // int y = 0b1;
        System.out.println(y);


        /******unsinged operator== ">>>" *******/
    }
}
